#Arreglos
#Un arreglo o vector es una tupla o lista de n elementos del mismo tipo
#(En este curso usaremos las listas para representar arreglos). A los
#elementos de un arreglo se les llama componentes del arreglo.

#Ejemplos
#[]  #Arreglo vacio.
#[1, 0, 7, -2, 8]# Un arreglo de enteros de 5 componentes.
#[’Radio’, ’Lado’]# Un arreglo de cadenas de 2 componentes.
#[1.3, 2.4, -3.0, 4.5]# Un arreglo de reales de 4 componentes.
#[True, False, False]# Un arreglo de booleanos de 3
#componentes.


